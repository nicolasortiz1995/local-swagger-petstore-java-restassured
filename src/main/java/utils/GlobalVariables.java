package utils;

/**
 * Utility class for storing global variables used across the test suite.
 * This class provides static variables to hold values such as pet ID and status.
 */
public class GlobalVariables {

    /**
     *  Stores the ID of a pet, used as a global variable.
     */
    public static String idPet;

    /**
     * Stores the status of a pet, used as a global variable.
     */
    public static String statusPet;
}